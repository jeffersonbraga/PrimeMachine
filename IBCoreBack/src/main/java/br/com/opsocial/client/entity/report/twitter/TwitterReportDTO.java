package br.com.opsocial.client.entity.report.twitter;

import br.com.opsocial.ejb.entity.generic.Persistent;

public class TwitterReportDTO extends Persistent {

	private static final long serialVersionUID = 1L;

	private Long idTwitterReport;
	private String idProfile;
	private String property;
	private Long value;
	private Long date;
	private Long sinceId;
	
	public TwitterReportDTO() {
	}
	
	/**
	 * @return the idTwitterReport
	 */
	public Long getIdTwitterReport() {
		return idTwitterReport;
	}
	/**
	 * @param idTwitterReport the idTwitterReport to set
	 */
	public void setIdTwitterReport(Long idTwitterReport) {
		this.idTwitterReport = idTwitterReport;
	}
	/**
	 * @return the idProfile
	 */
	public String getIdProfile() {
		return idProfile;
	}
	/**
	 * @param idProfile the idProfile to set
	 */
	public void setIdProfile(String idProfile) {
		this.idProfile = idProfile;
	}
	/**
	 * @return the property
	 */
	public String getProperty() {
		return property;
	}
	/**
	 * @param property the property to set
	 */
	public void setProperty(String property) {
		this.property = property;
	}
	/**
	 * @return the value
	 */
	public Long getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(Long value) {
		this.value = value;
	}
	/**
	 * @return the date
	 */
	public Long getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(Long date) {
		this.date = date;
	}
	/**
	 * @return the sinceId
	 */
	public Long getSinceId() {
		return sinceId;
	}
	/**
	 * @param sinceId the sinceId to set
	 */
	public void setSinceId(Long sinceId) {
		this.sinceId = sinceId;
	}	
}
