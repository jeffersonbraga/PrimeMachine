package br.com.opsocial.client.entity.mailbox;

import br.com.opsocial.ejb.entity.generic.Persistent;

public class MessageDTO extends Persistent {
	
	private static final long serialVersionUID = 1L;
	
	private Long idMessage;
	private Long createdTime;
	private String userName;
	private String userId;
	private String message;
	private ConversationDTO conversationDTO;
	private String networkMessageId;
	
	public MessageDTO() {
	}

	public Long getIdMessage() {
		return idMessage;
	}

	public void setIdMessage(Long idMessage) {
		this.idMessage = idMessage;
	}

	public Long getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Long createdTime) {
		this.createdTime = createdTime;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public ConversationDTO getConversationDTO() {
		return conversationDTO;
	}

	public void setConversationDTO(ConversationDTO conversationDTO) {
		this.conversationDTO = conversationDTO;
	}

	public String getNetworkMessageId() {
		return networkMessageId;
	}

	public void setNetworkMessageId(String networkMessageId) {
		this.networkMessageId = networkMessageId;
	}
}
