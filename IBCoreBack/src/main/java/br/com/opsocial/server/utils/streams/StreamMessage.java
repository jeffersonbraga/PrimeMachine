package br.com.opsocial.server.utils.streams;

import java.io.Serializable;

import br.com.opsocial.server.utils.facebook.entities.Attachment;
import br.com.opsocial.server.utils.facebook.entities.Share;

public class StreamMessage implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private String idMessage;
	private Long createdTime;
	private String userName;
	private String userId;
	private String message;
	private String idConversation;
	private Attachment attachment;
	private Share share;
	
	public StreamMessage() {
		
	}

	public String getIdMessage() {
		return idMessage;
	}

	public void setIdMessage(String idMessage) {
		this.idMessage = idMessage;
	}

	public Long getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Long createdTime) {
		this.createdTime = createdTime;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getIdConversation() {
		return idConversation;
	}

	public void setIdConversation(String idConversation) {
		this.idConversation = idConversation;
	}

	public Attachment getAttachment() {
		return attachment;
	}

	public void setAttachment(Attachment attachment) {
		this.attachment = attachment;
	}

	public Share getShare() {
		return share;
	}

	public void setShare(Share share) {
		this.share = share;
	}
	
}
