package br.com.opsocial.server.utils.facebook.entities;

import java.io.Serializable;

public class Message implements Serializable {

	private static final long serialVersionUID = -4439828802558596424L;
	
	private String id;
	private String createdTime;
	private String message;
	private IdNameEntity from;
	private AttachmentData attachments;
	private ShareData shares;
	private MessageData messages;
	
	public Message() {
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(String createdTime) {
		this.createdTime = createdTime;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public IdNameEntity getFrom() {
		return from;
	}

	public void setFrom(IdNameEntity from) {
		this.from = from;
	}

	public AttachmentData getAttachments() {
		return attachments;
	}

	public void setAttachments(AttachmentData attachments) {
		this.attachments = attachments;
	}

	public ShareData getShares() {
		return shares;
	}

	public void setShares(ShareData shares) {
		this.shares = shares;
	}

	public MessageData getMessages() {
		return messages;
	}

	public void setMessages(MessageData messages) {
		this.messages = messages;
	}
	
}
