package br.com.opsocial.server.utils.facebook.entities;

import java.io.Serializable;

public class Conversation implements Serializable {

	private static final long serialVersionUID = 2066388629817605653L;
	
	private String id;
	private String link;
	private String snippet;
	private String updatedTime;
	private Integer messageCount;
	private Integer unreadCount;
	private Boolean canReply;
	private Boolean isSubscribed;
	private IdNameEntityData senders;
	private IdNameEntityData participants;
	private MessageData messages;
	
	public Conversation() {
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getSnippet() {
		return snippet;
	}

	public void setSnippet(String snippet) {
		this.snippet = snippet;
	}

	public String getUpdatedTime() {
		return updatedTime;
	}

	public void setUpdatedTime(String updatedTime) {
		this.updatedTime = updatedTime;
	}

	public Integer getMessageCount() {
		return messageCount;
	}

	public void setMessageCount(Integer messageCount) {
		this.messageCount = messageCount;
	}

	public Integer getUnreadCount() {
		return unreadCount;
	}

	public void setUnreadCount(Integer unreadCount) {
		this.unreadCount = unreadCount;
	}

	public Boolean getCanReply() {
		return canReply;
	}

	public void setCanReply(Boolean canReply) {
		this.canReply = canReply;
	}

	public Boolean getIsSubscribed() {
		return isSubscribed;
	}

	public void setIsSubscribed(Boolean isSubscribed) {
		this.isSubscribed = isSubscribed;
	}

	public IdNameEntityData getSenders() {
		return senders;
	}

	public void setSenders(IdNameEntityData senders) {
		this.senders = senders;
	}

	public IdNameEntityData getParticipants() {
		return participants;
	}

	public void setParticipants(IdNameEntityData participants) {
		this.participants = participants;
	}

	public MessageData getMessages() {
		return messages;
	}

	public void setMessages(MessageData messages) {
		this.messages = messages;
	}
}
