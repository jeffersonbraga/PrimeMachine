package br.com.opsocial.server.utils.instagram.entities;

import java.io.Serializable;

public class User implements Serializable {

	private static final long serialVersionUID = 6695612899018549130L;

	private String name;
	private String username;
	private Long igId;
	private Long followersCount;
	private Long followsCount;
	private Long mediaCount;
	private String profilePictureUrl;
	private String id;
	
	public User() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public Long getIgId() {
		return igId;
	}

	public void setIgId(Long igId) {
		this.igId = igId;
	}

	public Long getFollowersCount() {
		return followersCount;
	}

	public void setFollowersCount(Long followersCount) {
		this.followersCount = followersCount;
	}

	public Long getFollowsCount() {
		return followsCount;
	}

	public void setFollowsCount(Long followsCount) {
		this.followsCount = followsCount;
	}

	public Long getMediaCount() {
		return mediaCount;
	}

	public void setMediaCount(Long mediaCount) {
		this.mediaCount = mediaCount;
	}

	public String getProfilePictureUrl() {
		return profilePictureUrl;
	}

	public void setProfilePictureUrl(String profilePictureUrl) {
		this.profilePictureUrl = profilePictureUrl;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}
