package br.com.opsocial.server.utils.streams;

import java.io.Serializable;

public class StreamMessageAux implements Serializable {

	private static final long serialVersionUID = 1L;

	private StreamMessage streamMessage;
	private StreamConversation streamConversation;
	private StreamPostPaging streamPostPaging;
	private Long idProfile;
	
	public StreamMessageAux() {
	}

	public StreamMessage getStreamMessage() {
		return streamMessage;
	}

	public StreamConversation getStreamConversation() {
		return streamConversation;
	}

	public void setStreamConversation(StreamConversation streamConversation) {
		this.streamConversation = streamConversation;
	}

	public void setStreamMessage(StreamMessage streamMessage) {
		this.streamMessage = streamMessage;
	}

	public StreamPostPaging getStreamPostPaging() {
		return streamPostPaging;
	}

	public void setStreamPostPaging(StreamPostPaging streamPostPaging) {
		this.streamPostPaging = streamPostPaging;
	}

	public Long getIdProfile() {
		return idProfile;
	}

	public void setIdProfile(Long idProfile) {
		this.idProfile = idProfile;
	}
	
}
