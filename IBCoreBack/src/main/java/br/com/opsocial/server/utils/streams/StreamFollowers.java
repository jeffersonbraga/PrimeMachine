package br.com.opsocial.server.utils.streams;

import java.io.Serializable;
import java.util.List;

public class StreamFollowers implements Serializable {
	
	private static final long serialVersionUID = 1L;

	private List<StreamFollower> followers;
	private StreamPostPaging paging;
	
	public StreamFollowers() {
	}

	public List<StreamFollower> getFollowers() {
		return followers;
	}

	public void setFollowers(List<StreamFollower> followers) {
		this.followers = followers;
	}

	public StreamPostPaging getPaging() {
		return paging;
	}

	public void setPaging(StreamPostPaging paging) {
		this.paging = paging;
	}
	
}
