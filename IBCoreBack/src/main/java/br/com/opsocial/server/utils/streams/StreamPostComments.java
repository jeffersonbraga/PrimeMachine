package br.com.opsocial.server.utils.streams;

import java.io.Serializable;
import java.util.List;

public class StreamPostComments implements Serializable {

	private static final long serialVersionUID = 1L; 
	
	private List<StreamPostComment> comments;
	private StreamPostPaging paging;
	
	public StreamPostComments() {
		
	}

	public List<StreamPostComment> getComments() {
		return comments;
	}

	public void setComments(List<StreamPostComment> comments) {
		this.comments = comments;
	}

	public StreamPostPaging getPaging() {
		return paging;
	}

	public void setPaging(StreamPostPaging paging) {
		this.paging = paging;
	}
	
}
