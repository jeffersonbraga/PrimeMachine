package br.com.opsocial.ejb.entity.socialnetworks.facebook;


public class PageImpressionsLocation {

	private String city = "";
	private String state = "";
	private String country = "";
	private Integer visualizations = 0;
	
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
	/**
	 * @return the country
	 */
	public String getCountry() {
		return country;
	}
	/**
	 * @param country the country to set
	 */
	public void setCountry(String country) {
		this.country = country;
	}
	/**
	 * @return the visualizations
	 */
	public Integer getVisualizations() {
		return visualizations;
	}
	/**
	 * @param visualizations the visualizations to set
	 */
	public void setVisualizations(Integer visualizations) {
		this.visualizations = visualizations;
	}
}
