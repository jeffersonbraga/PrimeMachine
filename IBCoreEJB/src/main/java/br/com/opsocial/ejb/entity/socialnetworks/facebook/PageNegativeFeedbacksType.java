package br.com.opsocial.ejb.entity.socialnetworks.facebook;

public class PageNegativeFeedbacksType {

	private Integer hideClicks;
	private Integer hideAllClicks;
	private Integer reportSpamClicks;
	
	public PageNegativeFeedbacksType() {
	
	}

	public Integer getHideClicks() {
		return hideClicks;
	}

	public void setHideClicks(Integer hideClicks) {
		this.hideClicks = hideClicks;
	}

	public Integer getHideAllClicks() {
		return hideAllClicks;
	}

	public void setHideAllClicks(Integer hideAllClicks) {
		this.hideAllClicks = hideAllClicks;
	}

	public Integer getReportSpamClicks() {
		return reportSpamClicks;
	}

	public void setReportSpamClicks(Integer reportSpamClicks) {
		this.reportSpamClicks = reportSpamClicks;
	}
	
}
