package br.com.opsocial.ejb.entity.socialnetworks.facebook;

public class PagePositiveFeedbacksType {
	
	private Integer like = 0;
	private Integer comment = 0;
	private Integer link = 0;
	private Integer answer = 0;
	private Integer claim = 0;
	private Integer rsvp = 0;
	
	public PagePositiveFeedbacksType() {
		
	}

	public Integer getLike() {
		return like;
	}

	public void setLike(Integer like) {
		this.like = like;
	}

	public Integer getComment() {
		return comment;
	}

	public void setComment(Integer comment) {
		this.comment = comment;
	}

	public Integer getLink() {
		return link;
	}

	public void setLink(Integer link) {
		this.link = link;
	}

	public Integer getAnswer() {
		return answer;
	}

	public void setAnswer(Integer answer) {
		this.answer = answer;
	}

	public Integer getClaim() {
		return claim;
	}

	public void setClaim(Integer claim) {
		this.claim = claim;
	}

	public Integer getRsvp() {
		return rsvp;
	}

	public void setRsvp(Integer rsvp) {
		this.rsvp = rsvp;
	}
	
}
