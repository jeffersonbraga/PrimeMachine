package br.com.opsocial.ejb.entity.report;

import br.com.opsocial.ejb.entity.generic.Persistent;

public class AgeGroup extends Persistent {

	private static final long serialVersionUID = 1L;
	
	private String initialAgeGroup;
	private String endAgeGroup;
	private Integer percentage = 0;
	private Long value = 0L;
	
	public AgeGroup() {
		
	}
	
	public AgeGroup(String initialAgeGroup, String endAgeGroup) {
		this.initialAgeGroup = initialAgeGroup;
		this.endAgeGroup = endAgeGroup;
	}

	public String getInitialAgeGroup() {
		return initialAgeGroup;
	}

	public void setInitialAgeGroup(String initialAgeGroup) {
		this.initialAgeGroup = initialAgeGroup;
	}

	public String getEndAgeGroup() {
		return endAgeGroup;
	}

	public void setEndAgeGroup(String endAgeGroup) {
		this.endAgeGroup = endAgeGroup;
	}

	public Integer getPercentage() {
		return percentage;
	}

	public void setPercentage(Integer percentage) {
		this.percentage = percentage;
	}

	public Long getValue() {
		return value;
	}

	public void setValue(Long value) {
		this.value = value;
	}

}
