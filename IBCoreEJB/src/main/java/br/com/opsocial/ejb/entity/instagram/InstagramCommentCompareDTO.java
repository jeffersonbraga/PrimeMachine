package br.com.opsocial.ejb.entity.instagram;

import java.math.BigDecimal;

import br.com.opsocial.ejb.entity.generic.Persistent;

public class InstagramCommentCompareDTO extends Persistent {

	private static final long serialVersionUID = 1L;

	private Long dateCompare;
	private BigDecimal followerComments;
	private BigDecimal noFollowerComments;

	public Long getDateCompare() {
		return dateCompare;
	}

	public BigDecimal getFollowerComments() {
		return followerComments;
	}

	public BigDecimal getNoFollowerComments() {
		return noFollowerComments;
	}

	public void setDateCompare(Long dateCompare) {
		this.dateCompare = dateCompare;
	}

	public void setFollowerComments(BigDecimal followerComments) {
		this.followerComments = followerComments;
	}

	public void setNoFollowerComments(BigDecimal noFollowerComments) {
		this.noFollowerComments = noFollowerComments;
	}
}