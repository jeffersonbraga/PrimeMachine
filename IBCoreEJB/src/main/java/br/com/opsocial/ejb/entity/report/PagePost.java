package br.com.opsocial.ejb.entity.report;

import br.com.opsocial.ejb.entity.generic.Persistent;
import br.com.opsocial.ejb.entity.socialnetworks.facebook.FacebookPicture;

public class PagePost extends Persistent {

	private static final long serialVersionUID = 1L;
	
	private String postId;
	private String message;
	private String type;
	private String permalink;
	private Long createdTime;
	private FacebookPicture facebookPicture;
	private Integer impressions = 0;
	private Integer consumptions = 0;
	private Integer stories = 0;
	private Double engagement;
	private Integer likes;
	private Integer comments;
	private Integer shares;
	
	public PagePost() {
	}
	
	/**
	 * @return the postId
	 */
	public String getPostId() {
		return postId;
	}
	/**
	 * @param postId the postId to set
	 */
	public void setPostId(String postId) {
		this.postId = postId;
	}
	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}
	/**
	 * @param message the message to set
	 */
	public void setMessage(String message) {
		this.message = message;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the permalink
	 */
	public String getPermalink() {
		return permalink;
	}
	/**
	 * @param permalink the permalink to set
	 */
	public void setPermalink(String permalink) {
		this.permalink = permalink;
	}
	/**
	 * @return the createdTime
	 */
	public Long getCreatedTime() {
		return createdTime;
	}
	/**
	 * @param createdTime the createdTime to set
	 */
	public void setCreatedTime(Long createdTime) {
		this.createdTime = createdTime;
	}	
	/**
	 * @return the facebookPicture
	 */
	public FacebookPicture getFacebookPicture() {
		return facebookPicture;
	}
	/**
	 * @param facebookPicture the facebookPicture to set
	 */
	public void setFacebookPicture(FacebookPicture facebookPicture) {
		this.facebookPicture = facebookPicture;
	}
	/**
	 * @return the impressions
	 */
	public Integer getImpressions() {
		return impressions;
	}
	/**
	 * @param impressions the impressions to set
	 */
	public void setImpressions(Integer impressions) {
		this.impressions = impressions;
	}
	/**
	 * @return the consumptions
	 */
	public Integer getConsumptions() {
		return consumptions;
	}
	/**
	 * @param consumptions the consumptions to set
	 */
	public void setConsumptions(Integer consumptions) {
		this.consumptions = consumptions;
	}
	/**
	 * @return the stories
	 */
	public Integer getStories() {
		return stories;
	}
	/**
	 * @param stories the stories to set
	 */
	public void setStories(Integer stories) {
		this.stories = stories;
	}
	/**
	 * @return the engagement
	 */
	public Double getEngagement() {
		return engagement;
	}
	/**
	 * @param engagement the engagement to set
	 */
	public void setEngagement(Double engagement) {
		this.engagement = engagement;
	}
	
	public Integer getLikes() {
		return likes;
	}
	
	public void setLikes(Integer likes) {
		this.likes = likes;
	}
	
	public Integer getComments() {
		return comments;
	}
	
	public void setComments(Integer comments) {
		this.comments = comments;
	}
	
	public Integer getShares() {
		return shares;
	}
	
	public void setShares(Integer shares) {
		this.shares = shares;
	}
}
