package br.com.opsocial.ejb.entity.instagram;

import br.com.opsocial.ejb.entity.generic.Persistent;

public class InstagramEngagedFollower extends Persistent {

	private static final long serialVersionUID = 1L;
	
	private InstagramFollower instagramFollower;
	private Long engagement;
	private Long likes;
	private Long comments;
	
	public InstagramEngagedFollower() {
		
	}

	public InstagramFollower getInstagramFollower() {
		return instagramFollower;
	}

	public void setInstagramFollower(InstagramFollower instagramFollower) {
		this.instagramFollower = instagramFollower;
	}

	public Long getEngagement() {
		return engagement;
	}

	public void setEngagement(Long engagement) {
		this.engagement = engagement;
	}

	public Long getLikes() {
		return likes;
	}

	public void setLikes(Long likes) {
		this.likes = likes;
	}

	public Long getComments() {
		return comments;
	}

	public void setComments(Long comments) {
		this.comments = comments;
	}
}
